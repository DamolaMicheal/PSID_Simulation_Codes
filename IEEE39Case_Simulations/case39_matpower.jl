ieee39_branch_key(fbus::Int, tbus::Int) = fbus < tbus ? (fbus, tbus) : (tbus, fbus)

function ieee39_branch_params(fbus::Int, tbus::Int)
    target = ieee39_branch_key(fbus, tbus)
    for row in IEEE39_BRANCH_DATA
        f = Int(row[1])#From bus
        t = Int(row[2])#To bus
        ieee39_branch_key(f, t) == target || continue
        return (r = row[3],#Line resistance[p.u.]
                x = row[4],#Line reactance[p.u.]
                b = row[5],#Line charging susceptance[p.u.]
                tap = row[9],#Transformer tap ratio
                name = "branch_$(f)_$(t)")#Branch name
    end
    error("Branch $(fbus)--$(tbus) was not found in IEEE39_BRANCH_DATA.")
end

function write_matpower_matrix(io, name::String, rows::Vector{Vector{Float64}})
    println(io, name * " = [")
    for row in rows
        vals = join(string.(row), "   ")
        println(io, "    " * vals * ";")
    end
    println(io, "];")
    println(io)
end

function write_modified_ieee39_case(path::String)
    open(path, "w") do io
        println(io, "function mpc = modified_ieeebus39case")
        println(io, "mpc.version = '2';")
        println(io, "mpc.baseMVA = $(MATPOWER_BASE_MVA);")
        println(io)

        println(io, "%% bus data")
        println(io, "% bus_i type Pd[MW] Qd[MVAr] Gs Bs area Vm[pu] Va[deg] baseKV zone Vmax[pu] Vmin[pu]")
        write_matpower_matrix(io, "mpc.bus", IEEE39_BUS_DATA)

        println(io, "%% generator data")
        println(io, "% bus Pg[MW] Qg[MVAr] Qmax[MVAr] Qmin[MVAr] Vg[pu] mBase[MVA] status Pmax[MW] Pmin[MW]")
        write_matpower_matrix(io, "mpc.gen", IEEE39_GEN_DATA)

        println(io, "%% branch data")
        println(io, "% fbus tbus r[pu] x[pu] b[pu] rateA[MVA] rateB[MVA] rateC[MVA] tap angle[deg] status angmin[deg] angmax[deg]")
        write_matpower_matrix(io, "mpc.branch", IEEE39_BRANCH_DATA)

        println(io, "%% generator cost data")
        println(io, "% model startup shutdown n c2 c1 c0")
        write_matpower_matrix(io, "mpc.gencost", IEEE39_GENCOST_DATA)

        println(io, "end")
    end
end